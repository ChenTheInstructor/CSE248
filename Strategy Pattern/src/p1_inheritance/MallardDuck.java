package p1_inheritance;

public class MallardDuck extends Duck {

	
	public void fly() {
		System.out.println("A mallardduck is flying.");
	}
	
	public void quack() {
		System.out.println("A mallardduck is quacking.");
	}
	
	public void display() {
		System.out.println("A mallardduck is displaying.");
	}
}
